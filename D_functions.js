// functions most commonly use in JS.

var1 = "function";
console.log(typeof(var1));      // typeof()

num = "1234";
console.log(Number(num));       // Number()
console.log(parseInt(num));     // parseInt()

console.log(String(num));       // String()

bool = "1";
console.log(Boolean(bool));    // Boolean()  :  change 1 to true and 0 to false.
console.log(+Boolean(bool));    // +Boolean() : change true to 1 and false to 0.

